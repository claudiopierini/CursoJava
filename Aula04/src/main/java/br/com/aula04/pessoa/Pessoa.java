package br.com.aula04.pessoa;

/**
 * @author java03
 *
 */
public class Pessoa {
	
	private String nome;

	/**
	 * @param nome
	 */
	public Pessoa(String nome) {
		this.nome = nome;
	}

	/**
	 * @return the nome
	 */
	public String getNome() {
		return nome;
	}

	/**
	 * @param nome the nome to set
	 */
	public void setNome(String nome) {
		this.nome = nome;
	}

}
