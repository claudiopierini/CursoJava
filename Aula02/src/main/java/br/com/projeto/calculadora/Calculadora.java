public class Calculadora {
	
	
	
	public double soma(int a, int b) {
		return a + b;
	}

	public double subtracao(int a, int b) {
		return a - b;
	}
	
	public double multiplicacao(int a, int b) {
		return a * b;
	}
	
	public double divisao(int a, int b) {
		return a / b;
	}
}
